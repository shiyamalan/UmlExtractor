public class Bill {

    private String dateOfUse;
    private String noOfIndividual;
    private String town;


    public String getDateOfUse() {
        return dateOfUse;
    }

    public void setDateOfUse(String dateOfUse) {
        this.dateOfUse = dateOfUse;
    }

    public String getNoOfIndividual() {
        return noOfIndividual;
    }

    public void setNoOfIndividual(String noOfIndividual) {
        this.noOfIndividual = noOfIndividual;
    }

    public String getTown() {
        return town;
    }

    public void setTown(String town) {
        this.town = town;
    }
}
